package com.example.anime.Entity.shoppingCartDataEntity;

public class ShoppingCartDataEntity {
    private String shoppingCartJinPinNumber;
    private String jinPinBuyingUser;
    private String jinPinPrice;
    private String jinPinPurchaseQuantity;
    private String jinPinTotalPrice;

    public String getShoppingCartJinPinNumber() {
        return shoppingCartJinPinNumber;
    }

    public void setShoppingCartJinPinNumber(String shoppingCartJinPinNumber) {
        this.shoppingCartJinPinNumber = shoppingCartJinPinNumber;
    }

    public String getJinPinBuyingUser() {
        return jinPinBuyingUser;
    }

    public void setJinPinBuyingUser(String jinPinBuyingUser) {
        this.jinPinBuyingUser = jinPinBuyingUser;
    }

    public String getJinPinPrice() {
        return jinPinPrice;
    }

    public void setJinPinPrice(String jinPinPrice) {
        this.jinPinPrice = jinPinPrice;
    }

    public String getJinPinPurchaseQuantity() {
        return jinPinPurchaseQuantity;
    }

    public void setJinPinPurchaseQuantity(String jinPinPurchaseQuantity) {
        this.jinPinPurchaseQuantity = jinPinPurchaseQuantity;
    }

    public String getJinPinTotalPrice() {
        return jinPinTotalPrice;
    }

    public void setJinPinTotalPrice(String jinPinTotalPrice) {
        this.jinPinTotalPrice = jinPinTotalPrice;
    }
}
