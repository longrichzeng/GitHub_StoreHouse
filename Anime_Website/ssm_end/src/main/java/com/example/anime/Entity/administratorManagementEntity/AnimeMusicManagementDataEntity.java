package com.example.anime.Entity.administratorManagementEntity;

public class AnimeMusicManagementDataEntity {
    private String musicNumber;
    private String musicName;
    private String musicType;
    private String musicSinger;
    private String musicRecommendIndex;

    public String getMusicNumber() {
        return musicNumber;
    }

    public void setMusicNumber(String musicNumber) {
        this.musicNumber = musicNumber;
    }

    public String getMusicName() {
        return musicName;
    }

    public void setMusicName(String musicName) {
        this.musicName = musicName;
    }

    public String getMusicType() {
        return musicType;
    }

    public void setMusicType(String musicType) {
        this.musicType = musicType;
    }

    public String getMusicSinger() {
        return musicSinger;
    }

    public void setMusicSinger(String musicSinger) {
        this.musicSinger = musicSinger;
    }

    public String getMusicRecommendIndex() {
        return musicRecommendIndex;
    }

    public void setMusicRecommendIndex(String musicRecommendIndex) {
        this.musicRecommendIndex = musicRecommendIndex;
    }
}
