package com.example.anime.Entity.individualInformationDataEntity;

public class AnnualPlanDataEntity {
    private String userName;
    private String location;
    private String planContent;
    private String planNotes;
    private String planGenerateDate;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getPlanContent() {
        return planContent;
    }

    public void setPlanContent(String planContent) {
        this.planContent = planContent;
    }

    public String getPlanNotes() {
        return planNotes;
    }

    public void setPlanNotes(String planNotes) {
        this.planNotes = planNotes;
    }

    public String getPlanGenerateDate() {
        return planGenerateDate;
    }

    public void setPlanGenerateDate(String planGenerateDate) {
        this.planGenerateDate = planGenerateDate;
    }
}
