public class AnimeDrameManagementDataDTO {
    private String animeName;
    private String animeType;
    private String animeAuthor;
    private String animePublishTime;
    private String animeRecommendIndex;
    private String animeIntroduction;

    public String getAnimeName() {
        return animeName;
    }

    public void setAnimeName(String animeName) {
        this.animeName = animeName;
    }

    public String getAnimeType() {
        return animeType;
    }

    public void setAnimeType(String animeType) {
        this.animeType = animeType;
    }

    public String getAnimeAuthor() {
        return animeAuthor;
    }

    public void setAnimeAuthor(String animeAuthor) {
        this.animeAuthor = animeAuthor;
    }

    public String getAnimePublishTime() {
        return animePublishTime;
    }

    public void setAnimePublishTime(String animePublishTime) {
        this.animePublishTime = animePublishTime;
    }

    public String getAnimeRecommendIndex() {
        return animeRecommendIndex;
    }

    public void setAnimeRecommendIndex(String animeRecommendIndex) {
        this.animeRecommendIndex = animeRecommendIndex;
    }

    public String getAnimeIntroduction() {
        return animeIntroduction;
    }

    public void setAnimeIntroduction(String animeIntroduction) {
        this.animeIntroduction = animeIntroduction;
    }
}
